int PuedeMover(int x, int y, int map[][COLS])
{
    int left = x / TILE_SIZE;
    int right = (x + 19) / TILE_SIZE;

    int top = y / TILE_SIZE;
    int bottom = (y + 27) / TILE_SIZE;

    if (left < 0 || right >= COLS ||
        top < 0 || bottom >= ROWS)
    {
        return 0;
    }

    if (map[top][left] == 1)
        return 0;
    if (map[top][right] == 1)
        return 0;
    if (map[bottom][left] == 1)
        return 0;
    if (map[bottom][right] == 1)
        return 0;

    return 1;
}
int CountPellets(void)
{
    int count = 0;

    for (int y = 0; y < ROWS; y++)
    {
        for (int x = 0; x < COLS; x++)
        {
            if (map[y][x] == 2)
            {
                count++;
            }
        }
    }

    return count;
}